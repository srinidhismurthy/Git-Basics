def sum_num(x,y):
    """
    
    :param x: 
    :param y: 
    :return: Sum of the two given numbers
    """
    return x+y

def mul_num(x,y):
    """
    
    :param x: 
    :param y: 
    :return:  Multiplication of the supplied numbers. 
    """
    return x*y

def sub_num(x,y):
    """
    Subtracts Number y from Number x
    :param x: 
    :param y: 
    :return: 
    """
    return x-y

def div_num(x,y):
    """
    
    :param x: 
    :param y: 
    :return:  Devision of number x by number y
    """
    return x/y

